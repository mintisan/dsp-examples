# The difference is: the Fourier Transform has a very high resolution in the frequency domain,
# and zero resolution in the time domain; we know at which frequencies the signal oscillates,
# but not at which time these oscillations occur. The output of a Wavelet transform hash a high
# resolution in the frequency domain and also in the time domain;
# it maintains information about the time-domain.
# The wavelet transform is better suited for analyzing signals with a dynamic frequency spectrum.




